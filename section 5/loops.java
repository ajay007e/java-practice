public class loops {
    
    public static void main(String[] args) {
        
        // while loop

        int i =0;
        while(i<5){
            System.out.println(i+1);
            i++;
        }

        // do while loop

        do{
            System.out.println(i+1);
            i++;
        }while(i<10);

        // for loop

        for(int j=0;j<10;j++){
            System.out.println(j+1);
        }
    }
}
